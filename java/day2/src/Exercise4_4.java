public class Exercise4_4 {
    public static void main(String[] args) {
        for (int i = 10; i > 0; i -= 2) {
            System.out.println(i);
        }

        int j=10;
        while (j>0) {
            System.out.println(j);
            j -= 2;
        }

        int k=10;
        do {
            System.out.println(k);
            k -= 2;
        }while (k>0);

    }
}
